public class Swap {
    public static void main(String args[]){
        int x = 10;
        int y = 20;
        int z =x;

        System.out.println("Value before swaping:");
        System.out.println(x);
        System.out.println(y);
        System.out.println("Values after swaping:");
        x =y;
        y = z;
        System.out.println(x);
        System.out.println(y);

    }
}
